#Calcular el area de un circulo
#Solicitar el radio del circulo
Radio = int(input("Ingrese el radio del circulo: "))

#Calculamos el area con la formula
Area = 3.1416 * Radio**2

#Muestro el resultado
print(f"El area del circulo es: {Area}")

